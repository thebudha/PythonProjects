# Operators
print(10 + 3)
print(10 - 3)
print(10 * 3)
print(10 / 3)  # The result of division with one slash is always a float
print(10 // 3)  # The result of division with two slashes is always an integer
print(10 % 3)  # The modulus is whatever number that is remaining after dividing
print(10 ** 3)  # Exponentiation

x = 10
x = x + 3
x += 3
